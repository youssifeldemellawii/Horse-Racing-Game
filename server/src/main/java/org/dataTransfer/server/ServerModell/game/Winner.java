package org.dataTransfer.server.ServerModell.game;

/**
 * Das Enum {@code Winner} definiert die möglichen Gewinner eines Spiels.
 * Es enthält die Farben {@code GREEN}, {@code BLUE}, {@code RED} und {@code YELLOW},
 * welche die verschiedenen möglichen Gewinner repräsentieren.
 * Mit {@code NONE} wird angezeigt, dass derzeit noch kein Gewinner feststeht.
 */
public enum Winner {
    GREEN,
    BLUE,
    RED,
    YELLOW,
    NONE
}
