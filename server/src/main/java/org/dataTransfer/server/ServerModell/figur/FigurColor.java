package org.dataTransfer.server.ServerModell.figur;

/**
 * Das Enum {@code FigurColor} definiert die möglichen Farben, die einer Spielfigur zugewiesen werden können.
 *
 * <ul>
 *     <li>{@code GREEN} – Repräsentiert die Farbe Grün.</li>
 *     <li>{@code BLUE} – Repräsentiert die Farbe Blau.</li>
 *     <li>{@code RED} – Repräsentiert die Farbe Rot.</li>
 *     <li>{@code YELLOW} – Repräsentiert die Farbe Gelb.</li>
 * </ul>
 */
public enum FigurColor {
	GREEN,
	BLUE,
	RED,
	YELLOW
}
